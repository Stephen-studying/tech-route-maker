    out.append(svg_text(width // 2, height - (24 if compact else 42), "Editable PPTX | SVG | Draw.io | HTML | Markdown | JSON", 13 if compact else 15, "#59636A", 600))
    out.append("</svg>")
    return "\n".join(out)


