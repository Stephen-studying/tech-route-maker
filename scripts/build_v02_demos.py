def render_demo(folder, route_factory):
    example_dir = ROOT / "examples" / folder
    out_dir = example_dir / "outputs"
    out_dir.mkdir(parents=True, exist_ok=True)
    route = route_factory()
    route_path = out_dir / "tech-route.json"
    write_text(route_path, json.dumps(route, ensure_ascii=False, indent=2) + "\n")
    subprocess.check_call([
        sys.executable,
        str(ROOT / "scripts" / "render_all.py"),
        str(route_path),
        str(out_dir),
        "--formats",
        CORE_FORMATS,
    ])


def svg_text(x, y, value, size, color, weight=400, anchor="middle"):
    return f'<text x="{x}" y="{y}" text-anchor="{anchor}" font-family="Arial, Helvetica, sans-serif" font-size="{size}" font-weight="{weight}" fill="{color}">{escape(value)}</text>'


def svg_rect(x, y, w, h, fill, stroke="#D7E0EA", rx=10, width=1.3):
    return f'<rect x="{x}" y="{y}" width="{w}" height="{h}" rx="{rx}" fill="{fill}" stroke="{stroke}" stroke-width="{width}"/>'


def asset_svg(width, height, compact=False):
    cards = [
        ("Academic", "#E8F3F1", "#315C61", ["Goal", "Data", "Method", "Validation"]),
        ("Proposal", "#F7E0E8", "#7BCFD0", ["Objective", "Content", "Technology", "Output"]),
        ("Engineering", "#E8EEF7", "#27456F", ["Boundary", "Model", "Control", "KPIs"]),
        ("Workflow", "#F4F5F7", "#475569", ["Intake", "Extract", "Render", "Review"]),
    ]
    title = "tech-route-maker" if compact else "Editable Technical Route Gallery"
    subtitle = "Evidence-grounded editable route diagrams for research and engineering"
    out = [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{height}" viewBox="0 0 {width} {height}">',
        '<rect width="100%" height="100%" fill="#FFFFFF"/>',
        svg_rect(18, 18, width - 36, height - 36, "#FFFFFF", "#D8E1EA", 18, 1),
        svg_text(width // 2, 66 if compact else 82, title, 30 if compact else 38, "#172033", 700),
        svg_text(width // 2, 104 if compact else 124, subtitle, 15 if compact else 17, "#59636A", 400),
    ]
    start_x = 66
    card_y = 146 if compact else 190
    card_w = 250 if compact else 270
    card_h = 164 if compact else 360
    gap = 28 if compact else 32
    for i, (name, fill, accent, labels) in enumerate(cards):
        x = start_x + i * (card_w + gap)
        out.append(svg_rect(x, card_y, card_w, card_h, fill, "#C8D4E0", 14, 1.4))
        out.append(svg_rect(x + 18, card_y + 18, card_w - 36, 32, accent, accent, 8, 1))
        out.append(svg_text(x + card_w / 2, card_y + 39, name, 14, "#FFFFFF", 700))
        visible = labels[:3] if compact else labels
        for j, label in enumerate(visible):
            y = card_y + 68 + j * (34 if compact else 58)
            h = 24 if compact else 38
            out.append(svg_rect(x + 30, y, card_w - 60, h, "#FFFFFF", "#9DB3C5", 7, 1.1))
            out.append(svg_text(x + card_w / 2, y + h / 2 + 4, label, 11 if compact else 14, "#172033", 600))
            if j < len(visible) - 1:
                cx = x + card_w / 2
                out.append(f'<path d="M {cx} {y + h} L {cx} {y + (34 if compact else 58) - 2}" stroke="{accent}" stroke-width="1.6" marker-end="url(#arrow{i})"/>')
