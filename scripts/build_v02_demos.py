CORE_FORMATS = "pptx,svg,drawio,excalidraw,mermaid,html,markdown,json"


def write_text(path, text):
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8", newline="\n") as handle:
        handle.write(text)


def ev(path, note):
    return [
        {
            "kind": "source",
            "source_id": "source_1",
            "path": path,
            "locator": note,
            "quote_or_note": note,
        }
    ]


def node(node_id, label, detail, tag, path, note, confidence="high"):
    return {
        "id": node_id,
        "label": label,
        "detail": detail,
        "tag": tag,
        "node_type": tag,
        "confidence": confidence,
        "is_inferred": False,
        "evidence": ev(path, note),
    }


def stage(stage_id, title, order, summary, nodes):
    return {"id": stage_id, "title": title, "order": order, "summary": summary, "nodes": nodes}


def edge(edge_id, source, target, label, kind="flow", confidence="medium"):
    return {"id": edge_id, "from": source, "to": target, "label": label, "kind": kind, "confidence": confidence, "evidence": []}


def finalize(route):
    route.setdefault("route_version", "0.2.0")
    route.setdefault("assumptions", [])
    route.setdefault("unresolved_questions", [])
    route.setdefault("citations", [])
    route.setdefault("renderer_overrides", {})
    route["quality_report"] = build_quality_report(route)
    return route


